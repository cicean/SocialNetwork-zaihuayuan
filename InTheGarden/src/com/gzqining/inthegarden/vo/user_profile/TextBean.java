package com.gzqining.inthegarden.vo.user_profile;

public class TextBean {
	private String field_id;
	private String field_key;
	private String field_name;
	private String form_type;
	private String form_default_value;
	private String field_data;
	public String getField_id() {
		return field_id;
	}
	public void setField_id(String field_id) {
		this.field_id = field_id;
	}
	public String getField_key() {
		return field_key;
	}
	public void setField_key(String field_key) {
		this.field_key = field_key;
	}
	public String getField_name() {
		return field_name;
	}
	public void setField_name(String field_name) {
		this.field_name = field_name;
	}
	public String getForm_type() {
		return form_type;
	}
	public void setForm_type(String form_type) {
		this.form_type = form_type;
	}
	public String getForm_default_value() {
		return form_default_value;
	}
	public void setForm_default_value(String form_default_value) {
		this.form_default_value = form_default_value;
	}
	public String getField_data() {
		return field_data;
	}
	public void setField_data(String field_data) {
		this.field_data = field_data;
	}
}
